package b_operator;
/*shift  각 비트의 값을 이동하는 연산자
 * 
 */
public class Ex04_Shift {

	public static void main(String[] args) {
		
		int a = 4;
		int b = a<<2;
		int c = a>>2;
		int d = a>>>1;
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("c="+c);
		System.out.println("d="+d);

	}

}
