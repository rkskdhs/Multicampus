package a_datatype;

public class Ex02_Caution {

	public static void main(String[] args) {
		//문자형 변수에 정수를 대입하면
	  char ch =97;
	  System.out.println("ch의 값:" +ch);
	  
	  //정수형 변수에 문자를 대입하면
	  int  su= 'B';
	  System.out.println("su의 값:" + su);
	  //실수형 double, float
	  float f = 3.6f; 
	 System.out.println(f);
	 
	  int i = (int) 3.6;
	  System.out.println(i);

	  //정수형
	  long big =1000000000L;
	  System.out.println(big);
	  
	}

}
